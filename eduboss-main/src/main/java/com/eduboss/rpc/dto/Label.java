package com.eduboss.rpc.dto;

/**
 * 包含ID和Text属性的对象，定义为Label
 * 
 * @author sk
 *
 */
public class Label {

	private String id;

	private String text;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

}
